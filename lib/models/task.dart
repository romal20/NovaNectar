class Task {
  String? id; // Changed to String to match Firestore document ID type
  String? title;
  String? note;
  int? isCompleted;
  String? date;
  String? startTime;
  String? endTime;
  int? color;
  int? remind;
  String? repeat;

  Task({
    this.id,
    this.title,
    this.note,
    this.isCompleted,
    this.date,
    this.startTime,
    this.endTime,
    this.color,
    this.remind,
    this.repeat,
  });

  // Deserialize JSON to Task object
  Task.fromJson(Map<String, dynamic> json) {
    id = json['id'];
    title = json['title'];
    note = json['note'];
    isCompleted = json['isCompleted'];
    date = json['date'];
    startTime = json['startTime'];
    endTime = json['endTime'];
    color = json['color'];
    remind = json['remind'];
    repeat = json['repeat'];
  }

  // Serialize Task object to JSON
  Map<String, dynamic> toJson() {
    return {
      'id': id ?? '', // Ensure id is not null for Firestore
      'title': title ?? '',
      'note': note ?? '',
      'isCompleted': isCompleted ?? 0, // Default to 0 if null
      'date': date ?? '',
      'startTime': startTime ?? '',
      'endTime': endTime ?? '',
      'color': color ?? 0, // Default to 0 if null
      'remind': remind ?? 0, // Default to 0 if null
      'repeat': repeat ?? '', // Default to empty string if null
    };
  }
}

